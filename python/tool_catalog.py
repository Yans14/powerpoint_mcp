def get_catalog_by_category() -> dict[str, list[dict[str, Any]]]:
    """Return tools grouped by category, preserving insertion order."""
    groups: dict[str, list[dict[str, Any]]] = {}
    for tool in TOOL_CATALOG:
        cat = tool["category"]
        groups.setdefault(cat, []).append(tool)
    return groups
