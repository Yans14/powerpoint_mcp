# -- Param helpers --


def _p(name: str, typ: str, required: bool = True, description: str = "", **kw: Any) -> dict[str, Any]:
    d: dict[str, Any] = {"name": name, "type": typ, "required": required, "description": description}
    d.update(kw)
    return d


def _pid(required: bool = True) -> dict[str, Any]:
    return _p("presentation_id", "uuid", required, "Session UUID", auto_fill="presentation_id")


def _si() -> dict[str, Any]:
    return _p("slide_index", "number", True, "1-based slide index")


def _shape_id_pair() -> list[dict[str, Any]]:
    return [
        _p("shape_name", "string", False, "Shape name"),
        _p("shape_id", "number", False, "Shape ID (alternative to shape_name)"),
    ]


